def get_random_bool
  [true, false].sample
end

def get_random_number
  (5..100).to_a.sample
end

def get_random_date
  year = rand(1970..2006)
  month = rand(1..12)
  day = rand(1..28)

  Date.new(year, month, day)
end

def create_sentence
  sentence = []
  (1..5).to_a.sample.times do
    sentence_words = []
    (10..20).to_a.sample.times do
      sentence_words << @words.sample
    end
    sentence << sentence_words.join(" ").capitalize + "."
  end
  sentence.join(" ")
end

def create_post_content
  post_content = []
  count = (2..10).to_a.sample
  count.times do |number|
    post_content << @post_sentences.sample
