def create_users(quantity)
  i = 1

  quantity.times do
    user_data = {
      email: "user_#{i}@email.com",
      password: "testtest",
    }

    if i == 1
      user_data[:admin] = true
    end

    user = User.create!(
      user_data
    )
    puts "User created with id #{user.id}"

    i = i + 1

    profile_data = {
      avatar: upload_random_image_profile_avatar,
      trust_points: get_random_number, 
      name: @names.sample, 
      surname: @surnames.sample, 
      description: @profile_descriptions.sample, 
      birthday: get_random_date, 
    }

    # Создание профиля для пользователя
    profile = user.create_profile!(profile_data)
    puts "Profile created for user with id #{profile.user.id}: #{profile_data[:name]} #{user.profile.birthday}"
  end
end

